public class Barang03 {
    private String kode;
    private String nama;
    private double harga;
    private int stok;

        public Barang03(String kode, String nama, double harga, int stok){
            this.kode = kode;
            this.nama = nama;
            this.harga = harga;
            this.stok = stok;
        }

            public String getKode(){
                return kode;
            }

            public String getNama(){
                return nama;
            }

            public double getHarga(){
                return harga;
            }

            public int getStok(){
                return stok;
            }
}


          

            